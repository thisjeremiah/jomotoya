// Deterministic PRNG. Same seed -> same world, always. This reproducibility is
// what makes the world debuggable and shareable now, and syncable later
// (multiplayer would sync small player-state structs over this same seeded world).

/** mulberry32 — small, fast, good enough for terrain/prop scatter. */
export function makeRng(seed: number): () => number {
  let a = seed >>> 0;
  return function () {
    a |= 0;
    a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

/** Stable hash so each edge can derive its own independent, repeatable stream. */
export function hashString(s: string): number {
  let h = 2166136261 >>> 0;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}
